import mongoose from "mongoose";

const { Schema } = mongoose;

const turismSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    desc: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    imageUser: {
      type: String,
      required: true,
    },
    imageArticle: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    username: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

//If the Post collection does not exist create a new one.
export default mongoose.models.Turism || mongoose.model("Turism", turismSchema);
